
// Data Types - It refers to the type of data we can store in memory.
// Most Common Data Types
// Int - Integer number
// Double - Decimal Number
// String - Text
// Char - Single character
// Boolean - true/false


fun main() {
    val myInt = 78
    val myDouble = 879.342
    val myString = "Shubham"
    val myChar = 'a'
    val myBoolean = false


    println(myInt)
    println(myDouble)
    println(myString)
    println(myChar)
    println(myBoolean)

}