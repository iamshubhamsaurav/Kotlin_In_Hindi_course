fun main() {
// Comparison operator = Used to compare two values
//    >	greater than
//    <	less than
//    >= greater than or equal to
//    <= less than or equal to
//    == equal to
//    != not equal to
    val a = 5
    val b = 5
    if (a != b) {
        println("a is not equal to b")
    } else {
        println("a is equal to b")
    }
}