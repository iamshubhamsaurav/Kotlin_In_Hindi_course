
fun main() {

    val day = 1

    when(day) {
     1 -> {
         println("Sunday")
         println("Line 2")
         println("Line 3")
     }
     2 -> println("Monday")
     3 -> println("Tuesday")
     4 -> println("Wednesday")
     5 -> println("Thursday")
     6 -> println("Friday")
     7 -> println("Saturday")
        else -> println("Invalid day")
    }
}